function toTraditionalText(value: string) {
  return value
    .split("")
    .map((character) => simplifiedToTraditionalCharacters[character] ?? character)
    .join("");
}

function toTraditionalValue<T>(value: T): T {
  if (typeof value === "string") {
    return toTraditionalText(value) as T;
  }

  if (Array.isArray(value)) {
    return value.map((item) => toTraditionalValue(item)) as T;
  }

  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value).map(([key, item]) => [
        key,
        toTraditionalValue(item),
      ]),
    ) as T;
  }

  return value;
}

const traditionalChineseDictionary = toTraditionalValue(dictionary["zh-Hans"]);

export function getDictionary(language: string | null | undefined) {
  const normalizedLanguage = normalizeLanguage(language);

  if (normalizedLanguage === "zh-Hans") {
    return dictionary["zh-Hans"];
  }

  if (normalizedLanguage === "zh-Hant") {
    return traditionalChineseDictionary;
  }

  if (normalizedLanguage === "es") {
    return dictionary.es;
  }

  return dictionary.en;
}
